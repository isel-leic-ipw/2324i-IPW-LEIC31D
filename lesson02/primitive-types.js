// let (Js) <=> var (Kotlin)    
// const (Js) <=> val (Kotlin)

let number = 12.3
console.log("typeof " + number + " is " + typeof number)  // number

let boolean = true
console.log("typeof " + boolean + " is " + typeof boolean) // boolean

let undef = undefined;
console.log("typeof " + undef + " is " + typeof undef)  // undefined

let string = "SLB"
console.log("typeof " + string + " is " + typeof string)    // string

let nil = null
console.log("typeof " + nil + " is " + typeof nil)      // object!!!

let object = { }

console.log("typeof " + object + " is " + typeof object)    // object

let array = [1,"abc", {}, function(){}] 
console.log("typeof " + array + " is " + typeof array)      // object!!!

let f = function name() { }
console.log("typeof " + f + " is " + typeof f)              // function



